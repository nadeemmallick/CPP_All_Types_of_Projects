// ADDITION OF TWO NUMBERS
// This program takes two integers as input from the user and outputs their sum.

#include <iostream>
using namespace std;
int main()
{
    int x, y;

    cout << "Enter the first number :" ;
    cin >> x;

    cout << "Enter the second number :" ;
    cin >> y;

    cout << "Sum of the two given number is :"<< x + y;
    return 0;
}